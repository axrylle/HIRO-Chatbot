    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        String lname = txtlname.getText();
        String fname = txtfname.getText();
        String mi = txtmi.getText();
        String bdate = txtbdate.getText();
        String age = txtage.getText();
        String gen = txtgender.getText();
        String blood = txtblood.getText();
        String address = txtaddress.getText();
        String contact = txtcontact.getText();
        String email = txtemail.getText();
        String medical  = txtMed.getText();
        // GETTING TEXT FROM JTEXTFIELD TO STRING - STRING INSERTED INTO VALUES FOR EACH KEY
          app.put("Last Name", lname);
          app.put("First Name", fname);
          app.put("Middle Initial", mi);
          app.put("Birthdate", bdate);
          app.put("Age", age + " years old");
          app.put("Gender", gen);
          app.put("Blood Type" , blood);
          app.put("Home Address", address);
          app.put("Contact Number", contact);
          app.put("Email Address" , email);
        //RADIOBUTTONS IF YES/NO - ALSO INSERTED INTO VALUES
        if(asthmarbtnYES.isSelected())
            app.put("Asthma" ,"Yes" );
         
        else if(asthmabtnNO.isSelected())
            app.put("Asthma", "No");
          
        if(heartrbtnYES.isSelected())
            app.put("Heart Problem" ,"Yes" );
        
        else if(heartbtnNO.isSelected())
            app.put("Heart Problem", "No"); 
        
        if(surgerybtnYES.isSelected())
            app.put("Past Surgeries" ,"Yes" );
         
        else if(surgeryrbtnNO.isSelected())
            app.put("Past Surgeries", "No");
          
            app.put("Past Medical History", medical);
        //USING ITERATION PARA MADISPLAY LAHAT NG LAMAN NG MAP SA TEXT AREA.
        for (Map.Entry a : app.entrySet() ){
            appform.append((String) a.getKey() +": "+ a.getValue() + "\n");
        }
          
        screentext.append("HIRO:    Are the information above correct? \n");
        panelinput.setVisible(false);
        exitpanel.setVisible(false);
        panelresult.setVisible(true);
        yesnopanel.setVisible(true);
        
    }//GEN-LAST:event_btnsubmitActionPerformed

