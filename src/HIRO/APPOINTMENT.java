    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        /*NAGINITIATE NG STRING PARA MAKUHA UNG VALUES NG FULL NAME
        GUMAMIT NG TRY AND CATCH PARA CHECK LANG UNG ERROR
        BUFFEREDWRITER PARA MA-SAVE SA FOLDER UNG TXT FILE NG APPOINTMENT FORM NI USER*/
        String l= txtlname.getText();
        String f = txtfname.getText();
        String m = txtmi.getText();
        try
        {
           BufferedWriter writer = new BufferedWriter(new FileWriter(l +", " + f +" "+ m + ".txt"));
            writer.append(appform.getText());
            writer.close();
            JOptionPane.showMessageDialog(null,"Appointment Form Saved", "Saved", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new START().setVisible(true);
        }
           
        catch (IOException except)
        {
            except.printStackTrace();
        }
    }//GEN-LAST:event_btnsaveActionPerformed

